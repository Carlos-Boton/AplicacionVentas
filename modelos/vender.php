public function insertar($idclientes,$comprobante,$fecha_hora,$total_venta,$idarticulo,$cantidad,$descripcion){
	$sql="INSERT INTO ventas (idcliente,idviaje,comprobante,fecha_hora,total_venta,estado) VALUES ('$idclientes','0','$comprobante','$fecha_hora','$total_venta','Pagado')";
	//return ejecutarConsulta($sql);
	 $idventanew=ejecutarConsulta_retornarID($sql);
	 $num_elementos=0;
	 $sw=true;
	 while ($num_elementos < count($idarticulo)) {

	 	$sql_detalle="INSERT INTO detalle_venta (idventa,idarticulo,cantidad,precio_venta) VALUES('$idventanew','$idarticulo[$num_elementos]','$cantidad[$num_elementos]','$descripcion[$num_elementos]')";

	 	ejecutarConsulta($sql_detalle) or $sw=false;

	 	$num_elementos=$num_elementos+1;
	 }
	 return $sw;
}

public function anular($idventa){
